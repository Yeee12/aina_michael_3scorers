class TeamStanding {
  final int rank;
  final String teamName;
  final String logo;
  final int matchesPlayed;
  final int wins;
  final int draws;
  final int losses;
  final int goalsFor;
  final int goalsAgainst;
  final int points;

  TeamStanding({
    required this.rank,
    required this.teamName,
    required this.logo,
    required this.matchesPlayed,
    required this.wins,
    required this.draws,
    required this.losses,
    required this.goalsFor,
    required this.goalsAgainst,
    required this.points,
  });
}
